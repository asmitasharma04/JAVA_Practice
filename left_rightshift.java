public class left_rightshift {
    public static void main(String[] args) {
        int g=20;
        int f=g<<1;
        System.out.println(f);
        int a=10;
        int b=30;
        int c=a&b;
        int d=a|b;
        int e=a^b;
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
